    public static class DayOfTheYearEx
    {
        public static int ToInt(this DayOfTheYear dayOfTheyear)
        {
            return (int)dayOfTheyear + 1;
        }

        public static DayOfTheYear FromInt(int i)
        {
            if (i > 366 || i < 1) throw new ArgumentException($"Invalid day of the year: {i}");
            return (DayOfTheYear)(i - 1);
        }

        public static string ToString(this DayOfTheYear dayOfTheYear)
        {
            int numeral = dayOfTheYear.ToInt();

            return $"Day {numeral}";
        }
    }
